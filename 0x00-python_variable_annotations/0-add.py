#!/usr/bin/env python3
"""
Module provides a function that returns the sum of two floats.
"""


def add(a: float, b: float) -> float:
    """
    Performs the sum of two floats and return result as a float.

    Args:
        a (float): First float number.
        b (float): Second float number.

    Returns:
        float: Sum of a and b.
    """
    return a + b
