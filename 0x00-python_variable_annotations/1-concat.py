#!/usr/bin/env python3
"""
Module to concat two strings and return the concatenated string.
"""


def concat(str1: str, str2: str) -> str:
    """
    Function that concatenates two strings.

    Args:
        str1: First string
        str2: Second string

    Returns:
        str: a concatenate string from the two initial strings
    """
    return str1 + str2
